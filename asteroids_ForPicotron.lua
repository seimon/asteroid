function space:init()
	self.spd_x=0.3
	self.spd_y=0
	self.stars={}
	self.particles={}

	local function make_star(i,max,base_spd)
		return {
			x=rnd(sw),
			y=rnd(sh),
			spd=base_spd+i/max*base_spd,
			size=1+rnd(1),
			c=rnd()<0.2 and 26 or 23
		}
	end
	for i=1,140 do add(self.stars,make_star(i,200,1)) end

	self:show(true)
	self:on("update",self.on_update)
end

